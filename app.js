function loadGoogleMaps() {

    const script =
        document.createElement("script");

    script.src =
        "https://maps.googleapis.com/maps/api/js?key=" +
        CONFIG.GOOGLE_MAPS_API_KEY +
        "&libraries=places";

    script.async = true;
    script.defer = true;

    script.onload = () => {

        console.log(
            "Google Maps 読み込み完了"
        );

        initializeAutocomplete();
    };

    document.head.appendChild(script);
}


