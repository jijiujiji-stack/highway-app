function calculateDistance(
    lat1,
    lon1,
    lat2,
    lon2
) {

    const R = 6371000;

    const dLat =
        (lat2 - lat1)
        * Math.PI / 180;

    const dLon =
        (lon2 - lon1)
        * Math.PI / 180;

    const a =

        Math.sin(dLat / 2)
        * Math.sin(dLat / 2)

        +

        Math.cos(lat1 * Math.PI / 180)

        *

        Math.cos(lat2 * Math.PI / 180)

        *

        Math.sin(dLon / 2)

        *

        Math.sin(dLon / 2);

    const c =
        2 *
        Math.atan2(
            Math.sqrt(a),
            Math.sqrt(1 - a)
        );

    return R * c;
}


function findNearestIcIndex(icArea) {

    if (
        currentLatitude === null ||
        currentLongitude === null
    ) {
        return 0;
    }

    const exits =
        IC_MASTER[icArea].exits
            .slice()
            .sort((a, b) =>
                (a.order ?? 999) - (b.order ?? 999)
            )
            .filter(exit =>
                exit.lat !== undefined &&
                exit.lng !== undefined
            );

    if (exits.length === 0) {
        return 0;
    }

    let nearestIndex = 0;
    let nearestDistance = Infinity;

    exits.forEach((exit, index) => {

        const distance =
            calculateDistance(
                currentLatitude,
                currentLongitude,
                exit.lat,
                exit.lng
            );

        if (distance < nearestDistance) {
            nearestDistance = distance;
            nearestIndex = index;
        }
    });

    lastNearestIcName =
        exits[nearestIndex].displayName;

    lastNearestIcDistanceKm =
        Math.round(nearestDistance / 1000);

    console.log(
        "最寄りIC",
        lastNearestIcName,
        lastNearestIcDistanceKm + "km"
    );

    return Math.max(0, nearestIndex - 2);
}


function checkAutoReSearch() {

    if (
        lastSearchLatitude === null ||
        lastSearchLongitude === null
    ) {

        document
            .getElementById("nextUpdateInfo")
            .textContent =
            "比較後に表示";

        return;
    }

    const distance =
        calculateDistance(
            lastSearchLatitude,
            lastSearchLongitude,
            currentLatitude,
            currentLongitude
        );

    const elapsedSeconds =
        (
            Date.now()
            -
            lastSearchTime
        ) / 1000;

    const remainingDistance =
        Math.max(
            0,
            2000 - Math.round(distance)
        );

    const remainingSeconds =
        Math.max(
            0,
            180 - Math.round(elapsedSeconds)
        );

    let displayTimeText = "";

    if (remainingSeconds > 60) {

        const roundedSeconds =
            Math.ceil(remainingSeconds / 30) * 30;

        const minutes =
            Math.floor(roundedSeconds / 60);

        const seconds =
            roundedSeconds % 60;

        if (seconds === 0) {
            displayTimeText =
                minutes + "分";
        }
        else {
            displayTimeText =
                minutes + "分" + seconds + "秒";
        }

    }
    else if (remainingSeconds > 10) {

        const roundedSeconds =
            Math.ceil(remainingSeconds / 10) * 10;

        displayTimeText =
            roundedSeconds + "秒";

    }
    else {

        displayTimeText =
            remainingSeconds + "秒";

    }

    const displayDistanceText =
        remainingDistance >= 1000
            ? (remainingDistance / 1000).toFixed(1) + "km"
            : remainingDistance + "m";

    setDataUpdateStatus(
        displayDistanceText +
        " / " +
        displayTimeText,
        "data-update-normal"
    );

    document
        .getElementById("autoSearchCondition")
        .textContent =
        "あと " +
        remainingDistance +
        "m / あと " +
        remainingSeconds +
        "秒";

    console.log(
        "移動距離",
        Math.round(distance),
        "m"
    );

    console.log(
        "経過時間",
        Math.round(elapsedSeconds),
        "秒"
    );

    if (
        distance >= 2000 ||
        elapsedSeconds >= 180
    ) {

        console.log(
            "自動再検索実行"
        );

        setDataUpdateStatus(
            "更新中...",
            "data-update-working"
        );

        if (
            document.getElementById("autoCompareEnabled")?.checked
        ) {
            searchAutoExitIcComparison(false);
        }
        else {
            searchFromCurrentLocation();
        }
    }
}


function shortenLocationName(address) {

    if (!address) {
        return "位置不明";
    }

    let text = address
        .replace("日本、", "")
        .replace(/〒\d{3}-\d{4}\s*/, "");

    const wardMatch =
        text.match(/東京都(.+?区)/);

    if (wardMatch) {
        return wardMatch[1] + "付近";
    }

    const cityMatch =
        text.match(/(.+?[市町村])/);

    if (cityMatch) {
        return cityMatch[1] + "付近";
    }

    return text;
}

function shortenDestinationName(text) {

    if (!text) {
        return "未設定";
    }

    let name = text
        .replace("日本、", "")
        .replace(/〒\d{3}-\d{4}\s*/, "");

    const parts =
        name.split(" ");

    return parts[parts.length - 1];
}

