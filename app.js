async function searchMultiExitIcComparison() {

    if (
        currentLatitude === null ||
        currentLongitude === null
    ) {
        alert("現在地取得待ちです");
        return;
    }

    const destination =
        document
            .getElementById("destination")
            .value;

    const origin =
        document
            .getElementById("origin")
            .value;

    const exitIcListText =
        document
            .getElementById("exitIcList")
            .value;

    const acceptableDelay =
        Number(
            document
                .getElementById("acceptableDelay")
                .value
        );

    if (!destination) {
        alert("目的地を入力してください");
        return;
    }

    if (!exitIcListText) {
        alert("降りるIC候補を入力してください");
        return;
    }

    const exitIcList =
        exitIcListText
            .split(",")
            .map(text => text.trim())
            .filter(text => text !== "");

    if (exitIcList.length === 0) {
        alert("降りるIC候補を入力してください");
        return;
    }

    try {

        document
            .getElementById("multiExitIcResult")
            .textContent =
            "計算中...";

        const keepHighwayRoute =
            await getHighwayRouteFromOrigin(
                origin,
                destination
            );

        const keepHighwayMinutes =
            Math.round(
                parseInt(
                    keepHighwayRoute.duration.replace("s", "")
                ) / 60
            );

        const results = [];

        for (const exitIc of exitIcList) {

            try {

                const highwayToIcRoute =
                    await getHighwayRouteFromOrigin(
                        origin,
                        exitIc
                    );

                const localFromIcRoute =
                    await getLocalRoute(
                        exitIc,
                        destination
                    );

                const highwayToIcMinutes =
                    Math.round(
                        parseInt(
                            highwayToIcRoute.duration.replace("s", "")
                        ) / 60
                    );

                const localFromIcMinutes =
                    Math.round(
                        parseInt(
                            localFromIcRoute.duration.replace("s", "")
                        ) / 60
                    );

                const totalMinutes =
                    highwayToIcMinutes +
                    localFromIcMinutes;

                const difference =
                    totalMinutes -
                    keepHighwayMinutes;

                const keepHighwayKm =
                    keepHighwayRoute.distanceMeters / 1000;

                const highwayToIcKm =
                    highwayToIcRoute.distanceMeters / 1000;

                const keepHighwayToll =
                    Math.round(keepHighwayKm * 24);

                const exitIcToll =
                    Math.round(highwayToIcKm * 24);

                const savedToll =
                    keepHighwayToll - exitIcToll;

                let yenPerExtraMinute = 0;

                if (difference > 0) {
                    yenPerExtraMinute =
                        Math.round(savedToll / difference);
                }

                results.push({
                    exitIc: exitIc,
                    totalMinutes: totalMinutes,
                    difference: difference,
                    savedToll: savedToll,
                    yenPerExtraMinute: yenPerExtraMinute
                });

            } catch (error) {

                console.warn(
                    "IC比較失敗:",
                    exitIc,
                    error.message
                );
            }
        }


        if (results.length === 0) {
            throw new Error(
                "比較できるIC候補がありませんでした"
            );
        }

        displayMultiExitIcComparison(
            keepHighwayMinutes,
            results,
            acceptableDelay
        );

        document
            .getElementById("autoSearchTime")
            .textContent =
            new Date().toLocaleTimeString("ja-JP");

    } catch (error) {

        console.error(error);

        setDataUpdateStatus(
            "更新失敗",
            "data-update-error"
        );

        alert(
            "複数IC比較に失敗しました\n\n" +
            error.message
        );
    }
}

