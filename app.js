async function reverseGeocode(
    lat,
    lng
) {
    return new Promise((resolve) => {

        const geocoder =
            new google.maps.Geocoder();

        geocoder.geocode(
            {
                location: {
                    lat: lat,
                    lng: lng
                }
            },
            (results, status) => {

                console.log(
                    "Geocoder結果",
                    status,
                    results
                );

                if (
                    status === "OK" &&
                    results &&
                    results.length > 0
                ) {
                    resolve(
                        results[0].formatted_address
                    );
                    return;
                }

                resolve("位置不明");
            }
        );
    });
}


