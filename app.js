function getCandidateStartIndex(origin, icArea) {

    if (origin) {

        if (icArea === "joban") {

            if (
                origin.includes("石岡") ||
                origin.includes("土浦")
            ) {
                return 7;
            }

            if (
                origin.includes("つくば") ||
                origin.includes("牛久")
            ) {
                return 5;
            }

            if (
                origin.includes("守谷") ||
                origin.includes("取手") ||
                origin.includes("柏") ||
                origin.includes("流山")
            ) {
                return 1;
            }

            return 0;
        }

        return 0;
    }

    const currentLocationText =
        document
            .getElementById("currentLocation")
            .textContent;

    if (icArea === "joban") {

        if (
            currentLocationText.includes("石岡") ||
            currentLocationText.includes("土浦")
        ) {
            return 7;
        }

        if (
            currentLocationText.includes("つくば") ||
            currentLocationText.includes("牛久")
        ) {
            return 5;
        }

        if (
            currentLocationText.includes("守谷") ||
            currentLocationText.includes("取手") ||
            currentLocationText.includes("柏") ||
            currentLocationText.includes("流山")
        ) {
            return 1;
        }

        return findNearestIcIndex(icArea);
    }


    if (
        lastSearchLatitude === null ||
        currentLatitude === null
    ) {
        return 0;
    }

    const distanceKm =
        calculateDistance(
            lastSearchLatitude,
            lastSearchLongitude,
            currentLatitude,
            currentLongitude
        ) / 1000;

    if (distanceKm >= 40) {
        return 2;
    }

    if (distanceKm >= 20) {
        return 1;
    }

    return 0;
}

